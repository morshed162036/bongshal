							<div class="blog-article__abstract">
								<p> Whether you welcome them as new competition for established brands, reject them out of hand, or remain skeptical for now, the fact is that Chinese motorcycles are here to stay in the U.S. market.</p>
							</div>
							<p> In this edition of the Highside/Lowside motorcycle podcast, Spurgeon and Zack dive into the topic of motorcycles built in China that are now available in the United States. From the biggest players, such as CFMOTO, to former European brands that are now producing motorcycles in China, they take a look at what's available and how the ones they've personally ridden stack up to the better known competition. They also raise the question of whether the Chinese motorcycles of today are in the same stage as the first Japanese motorcycles that came to the U.S. market in the 1960s and, from humble beginnings, went on to world domination. And finally, the big question: Should you buy a Chinese motorcycle?</p>
							<p> All that plus listener comments, the engine sound guessing game, and more in this episode of Highside/Lowside.</p>
